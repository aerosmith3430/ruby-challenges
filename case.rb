fav_weather = 'rain'
case(fav_weather)
when 'sunny'
	puts "Wear less."
when 'snow'
	puts "Wear more."
when 'rain'
	puts "Wear a jacket with a hood.  Umbrellas are for wimps."
when 'fog'
	puts "Drive carefully."
when 'wind'
	puts "Do not use an umbrella lest you end up like Mary Poppins."	
when 'sleet'
	puts "Prepare to wear multiple jackets when the power goes out."
when 'thunder'
	puts "Wear earplugs."
when 'lightning'
	puts "Don't play golf."
else
	puts "Stop trying to predict the unpredictable."
end